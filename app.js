// 持込制度の状態判定
// 戻り値: {state, label, ico, css}
//   state: 'active'(あり)/'half'(半額)/'weight'(重量制)/'suspended'(停止中)/'none'(なし)
function classifyCarryIn(text) {
  const s = String(text || '').trim();
  if (!s || s === '-' || /制度なし|持込[^可]*なし|戸別[^→]*のみ|不可/.test(s)) {
    return {state:'none', label:'持込制度なし', ico:'🚫', css:'none'};
  }
  if (/停止|休止|当面|中止/.test(s)) {
    return {state:'suspended', label:'持込停止中', ico:'⛔', css:'suspended'};
  }
  if (/半額|半分|割引|減額/.test(s)) {
    return {state:'half', label:'持込で割引', ico:'💰', css:'half'};
  }
  if (/\d+\s*kg/i.test(s) || /重量|kg.*\d|\d.*kg/.test(s)) {
    return {state:'weight', label:'重量制で持込可', ico:'⚖️', css:'weight'};
  }
  return {state:'active', label:'持込可', ico:'✅', css:''};
}

