let CITIES = [];
async function loadCitiesIndex() {
  const indexFiles = [
    'cities_kanto.json',
    'cities_osaka.json',
    'cities_tokai.json',
    'cities_kinki.json',
    'cities_kyushu.json',
    'cities_hokkaido.json',
    'cities_tohoku.json',
    'cities_chugoku.json',
    'cities_shikoku.json',
    'cities_koshinetsu.json',
  ];
  const all = [];
  const ver = window.APP_DATA_VER || Date.now();
  for (const f of indexFiles) {
    try {
      const r = await fetch(`./data/${f}?v=${ver}`);
      if (r.ok) { const d = await r.json(); all.push(...(d.cities || [])); }
    } catch(e) {}
  }
  CITIES = all;
  // フォールバック/補完: 東京都49都市が CITIES に欠けていたら TOKYO_CITIES から補う
  const present = new Set(CITIES.map(c => c.id));
  TOKYO_CITIES.forEach(c => {
    if (!present.has(c.id)) {
      CITIES.push({
        id: c.id, pref: '13', prefName: '東京都', name: c.name,
        rank: c.rank, pop: c.pop, dataReady: true, scrapeUrl: c.scrapeUrl
      });
    } else {
      // 既存エントリに dataReady=true を強制（v2.json 完備済みのため）
      const idx = CITIES.findIndex(x => x.id === c.id);
      if (idx >= 0) CITIES[idx].dataReady = true;
    }
  });
}

