function norm(s) {
  let r = String(s||'').toLowerCase()
    .replace(/[ァ-ヶ]/g, c => String.fromCharCode(c.charCodeAt(0) - 0x60))
    .replace(/[ー・]/g, '').replace(/\s+/g, '');
  const dk={'が':'か','ぎ':'き','ぐ':'く','げ':'け','ご':'こ','ざ':'さ','じ':'し','ず':'す','ぜ':'せ','ぞ':'そ',
    'だ':'た','ぢ':'ち','づ':'つ','で':'て','ど':'と','ば':'は','び':'ひ','ぶ':'ふ','べ':'へ','ぼ':'ほ',
    'ぱ':'は','ぴ':'ひ','ぷ':'ふ','ぺ':'へ','ぽ':'ほ','ゔ':'う'};
  const sm={'ぁ':'あ','ぃ':'い','ぅ':'う','ぇ':'え','ぉ':'お','っ':'つ','ょ':'よ','ゃ':'や','ゅ':'ゆ'};
  return r.replace(/./g, c => dk[c]||sm[c]||c);
}
// マイルド正規化: 濁点・長音は保持（ガーデン≠カーテン を区別できる精度重視版）
function normMild(s) {
  return String(s||'').toLowerCase()
    .replace(/[ァ-ヶ]/g, c => String.fromCharCode(c.charCodeAt(0) - 0x60))
    .replace(/[・\s]/g, '');
}
// 品目名から括弧内のサイズ・条件表記を除いた基本名（「カーペット（6畳未満）」→「カーペット」）
function baseName(item){ return String(item.n||'').replace(/（[^）]*）|\([^)]*\)/g, ''); }
function itemText(item){ return [item.n,...(item.tags||[]),item.note||''].join(' '); }

function itemEnText(item){ return [item.en||EN_NAMES[item.n]||'',...(item.tags||[]).filter(t=>/[a-z]/i.test(t))].join(' ').toLowerCase(); }

function searchItems(q, items) {
  if (!q.trim()) return { results: items, mode: 'all' };

  // English keyword search
  if (/[a-zA-Z]/.test(q)) {
    const ql = q.toLowerCase().trim();
    const enDirect = items.filter(i => {
      const en = (item => item.en||EN_NAMES[item.n]||'')(i).toLowerCase();
      return en.includes(ql) || itemEnText(i).includes(ql);
    });
    if (enDirect.length) return { results: enDirect, mode: 'direct' };
    // English synonym lookup
    const enKey = Object.keys(SYNONYMS_EN).find(k => k===ql || ql.includes(k) || k.includes(ql));
    if (enKey) {
      const jWords = SYNONYMS_EN[enKey];
      const enSyn = items.filter(i => { const t=norm(itemText(i)); return jWords.some(w=>t.includes(norm(w))); });
      if (enSyn.length) return { results: enSyn, mode: 'synonym', label: `"${q}" → ${jWords.slice(0,3).join(' / ')}` };
    }
  }

  const nq = norm(q);
  if (!nq) return { results: items, mode: 'all' };
  const mq = normMild(q);

  // --- 同義語キーの選定: 完全一致キーを最優先。部分一致は比率・長さで制限し
  //     「キャンプチェア」が短いキー「ちえあ」等に飲まれて無差別展開されるのを防ぐ ---
  const allKeys = Object.keys(SYNONYMS);
  let useKeys = allKeys.filter(k => norm(k) === nq);
  if (!useKeys.length) {
    useKeys = allKeys.filter(k => {
      const nk = norm(k);
      return (nk.includes(nq) && nq.length >= 2) ||
             (nq.includes(nk) && nk.length >= 3 && nk.length >= nq.length * 0.6);
    });
  }
  const expWords = useKeys.length
    ? [...new Set(useKeys.flatMap(k => SYNONYMS[k]))].map(w => ({ raw: w, mild: normMild(w) })).filter(w => w.mild)
    : [];

  // --- スコア式ランキング ---
  // 100=直接一致(マイルド) / 90=同義語(複数語+品目名一致) / 80=直接一致(あいまい正規化)
  // 75=連結テキスト一致 / 70=前方一致 / 62=同義語(弱) / 60=タグ部分一致 / 55以下=弱一致
  const scored = [];
  items.forEach((item, idx) => {
    const bn = baseName(item);
    const bnM = normMild(bn), bnN = norm(bn);
    const fullM = normMild(item.n), fullN = norm(item.n);
    const tags = item.tags || [];
    const tagsM = tags.map(normMild), tagsN = tags.map(norm);
    const joinedM = normMild(itemText(item)), joinedN = norm(itemText(item));

    let d = 0; // 直接一致系
    // 1文字クエリ（例:「畳」）は語頭・語尾・完全一致のみ強一致とし、
    // 「折畳ベッド」のような語中一致は弱一致(58)に落とす
    const strongM = mq && bnM.includes(mq) && (mq.length >= 2 || bnM === mq || bnM.startsWith(mq) || bnM.endsWith(mq));
    const strongN = bnN.includes(nq) && (nq.length >= 2 || bnN === nq || bnN.startsWith(nq) || bnN.endsWith(nq));
    if (mq && (strongM || tagsM.some(t => t === mq))) d = 100;
    else if (strongN || tagsN.some(t => t === nq)) d = 80;
    else if ((mq && bnM.includes(mq)) || bnN.includes(nq)) d = 58;
    else if (mq.length >= 2 && joinedM.includes(mq)) d = 75;
    else if (nq.length >= 2 && String(item.n).split(/[・\s（）()／\/]+/).some(w => w && (normMild(w).startsWith(mq) || norm(w).startsWith(nq)))) d = 70;
    else if ((mq && tagsM.some(t => t.includes(mq))) || tagsN.some(t => t.includes(nq))) d = 60;
    else if (nq.length >= 2 && joinedN.includes(nq)) d = 55;

    let s = 0, cnt = 0, nameHit = false; // 同義語一致（マイルド正規化のみ＝誤爆防止）
    for (const w of expWords) {
      const inName = fullM.includes(w.mild);
      if (inName || tagsM.some(t => t.includes(w.mild))) { cnt++; if (inName) nameHit = true; }
    }
    if (cnt >= 2 && nameHit) s = 90;
    else if (cnt >= 1) s = 62;

    let wk = 0; // 弱一致（括弧内サイズ表記・備考のみの一致 例:「6畳未満」）
    if ((mq && fullM.includes(mq)) || fullN.includes(nq) || norm(item.note || '').includes(nq)) wk = 45;

    const score = Math.max(d, s, wk);
    if (score > 0) scored.push({ item, score, d, s, cnt, idx });
  });

  if (scored.length) {
    scored.sort((a, b) => b.score - a.score || b.cnt - a.cnt || a.idx - b.idx);
    const top = scored[0];
    const strongCnt = scored.filter(x => x.score >= 70).length;
    let main = scored, rest = [];
    if (strongCnt >= 3) {
      // 強い候補が十分あるときは低スコアを「その他の候補」へ
      main = scored.filter(x => x.score >= 70);
      rest = scored.filter(x => x.score < 70);
    } else if (top.score >= 80) {
      // 直接一致があるときは括弧内サイズ表記のみの一致（「畳」→6畳未満 等）を折りたたみ
      main = scored.filter(x => x.score > 45);
      rest = scored.filter(x => x.score === 45);
    }
    const results = main.map(x => x.item);
    const others = rest.map(x => x.item);
    if (top.d >= 80) {
      const extras = main.filter(x => x.d < 70 && x.s > 0).length;
      if (extras) return { results, others, mode: 'expanded', label: `「${q}」+関連品目${extras}件` };
      return { results, others, mode: 'direct' };
    }
    if (top.s > 0 && top.s >= top.d && useKeys.length) {
      const vals = SYNONYMS[useKeys[0]];
      return { results, others, mode: 'synonym', label: `「${q}」= 「${vals.slice(0,3).join('・')}」として検索` };
    }
    return { results, others, mode: 'direct' };
  }

  // --- あいまい検索: 編集距離をクエリ長に比例制限（〜4字:1 / 5-7字:2 / 8字〜:3）
  //     かつ一致率60%未満は除外 ---
  if (nq.length >= 3) {
    const lenTol = nq.length <= 4 ? 1 : (nq.length <= 7 ? 2 : 3);
    const tol = Math.min(lenTol, Math.floor(nq.length * 0.4));
    const fz = [];
    for (const item of items) {
      const words = norm(itemText(item)).replace(/[（）]/g,' ').split(/\s+/);
      let minD = Infinity;
      for (const w of words) {
        if (w.length < 2) continue;
        for (let s=0;s<=w.length-nq.length;s++) minD=Math.min(minD,ldist(nq,w.substr(s,nq.length)));
        if (Math.abs(w.length-nq.length)<=2) minD=Math.min(minD,ldist(nq,w));
      }
      if (minD <= tol && (nq.length - minD) / nq.length >= 0.6) fz.push({item,dist:minD});
    }
    fz.sort((a,b)=>a.dist-b.dist);
    if (fz.length) {
      const topNames = [...new Set(fz.slice(0,3).map(x=>x.item.n))].join('、');
      return { results: fz.slice(0,12).map(x=>x.item), mode:'fuzzy', label:`「${q}」に近い品目: ${topNames}` };
    }
  }
  return { results:[], mode:'none' };
}

function ldist(a,b) {
  if(!a)return b.length; if(!b)return a.length;
  const dp=Array.from({length:a.length+1},(_,i)=>[i,...Array(b.length).fill(0)]);
  for(let j=1;j<=b.length;j++)dp[0][j]=j;
  for(let i=1;i<=a.length;i++)for(let j=1;j<=b.length;j++)
    dp[i][j]=a[i-1]===b[j-1]?dp[i-1][j-1]:1+Math.min(dp[i-1][j],dp[i][j-1],dp[i-1][j-1]);
  return dp[a.length][b.length];
}

