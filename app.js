// Phase B-2: 料金体系タイプ判定
// =====================================================
// typeA: 23区共通券（A¥200/B¥300） — 13101〜13123
// typeB: ポイント制（1pt=¥100） — 13201/13202/13203
// typeC: 単一額面券 — 13209町田(¥400)、13207昭島(¥500/¥1000)など
// typeD: 315円刻み — 13208調布のみ
// typeE: 特殊運用（処理券なし or 業者個別） — 13205青梅、13228あきる野
// typeF: 多種額面券（4額面以上） — その他多摩市町村
function getFeeSystemType(cityId, rules) {
  if (!cityId) return 'typeF';
  const id = String(cityId);
  // 23区
  const num = parseInt(id, 10);
  if (id.startsWith('13') && num >= 13101 && num <= 13123) return 'typeA';
  // ポイント制（八王子・立川・武蔵野）
  if (id === '13201' || id === '13202' || id === '13203') return 'typeB';
  // 315円刻み（調布のみ）
  if (id === '13208') return 'typeD';
  // 特殊運用（青梅・あきる野）
  if (id === '13205' || id === '13228') return 'typeE';
  // 単一額面券（町田・昭島）
  if (id === '13209' || id === '13207') return 'typeC';
  // ルールから推定（fallback）
  const r = rules || {};
  const pm = String(r.paymentMethod || '');
  if (/ポイント|1pt|1ポイント/.test(pm)) return 'typeB';
  if (/315/.test(pm)) return 'typeD';
  if (/処理券[^あ]*無|処理券制度なし|納付書/.test(pm)) return 'typeE';
  // デフォルトは多種額面券
  return 'typeF';
}

