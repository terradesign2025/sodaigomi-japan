// v2.json の構造ゆれを統一フォーマットに正規化
// パターンA: {city:{id,...,rules:{}, items:[...], uncollectible:[...]}, itemSet}
// パターンB: {city:{id,...,rules:{}}, items:[...], uncollectible:[...], itemSet}
function normalizeCityData(data) {
  if (!data || typeof data !== 'object') return null;
  const city = data.city || {};
  const cityItems = (city.items && Array.isArray(city.items)) ? city.items : null;
  const topItems = (data.items && Array.isArray(data.items)) ? data.items : null;
  const items = cityItems || topItems || [];
  const cityUnc = (city.uncollectible && Array.isArray(city.uncollectible)) ? city.uncollectible : null;
  const topUnc = (data.uncollectible && Array.isArray(data.uncollectible)) ? data.uncollectible : null;
  // uncollectible が文字列配列の都市（大阪市等）は {n:文字列} 形式に統一する
  // （文字列のままだと {...u} 展開で item.n が undefined になり空の⚠️行が表示されるため）
  const uncollectible = (cityUnc || topUnc || [])
    .map(u => typeof u === 'string' ? {n: u} : u)
    .filter(u => u && u.n);
  return {
    city: {
      id: city.id,
      name: city.name,
      prefecture: city.prefecture,
      rank: city.rank,
      dataVersion: city.dataVersion,
      rules: city.rules || {}
    },
    items: items,
    uncollectible: uncollectible,
    itemSet: data.itemSet || city.itemSet || ''
  };
}

