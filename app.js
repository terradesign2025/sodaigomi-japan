// Phase B-1: 都市データロード（v2.json優先・v1フォールバック・正規化済み）
async function loadCityData(cityId) {
  // 1. キャッシュ
  if (CITY_CACHE[cityId]) return CITY_CACHE[cityId];

  const prefId = cityId.substring(0, 2);
  const prefDir = PREF_DIR[prefId];
  if (!prefDir) return null;

  const ver = window.APP_DATA_VER || Date.now();

  // 2. v2.json を最優先で試行（city.items 内蔵想定）
  try {
    const v2Res = await fetch(`./cities/${prefDir}/${cityId}_v2.json?v=${ver}`);
    if (v2Res.ok) {
      const raw = await v2Res.json();
      const norm = normalizeCityData(raw);
      if (norm && norm.items && norm.items.length > 0) {
        CITY_CACHE[cityId] = norm;
        return norm;
      }
    }
  } catch(e) {
    console.warn(`v2 load failed for ${cityId}:`, e);
  }

  // 3. 旧形式 v1 (city.json + items_{itemSet}.json テンプレ参照) フォールバック
  try {
    const cityRes = await fetch(`./cities/${prefDir}/${cityId}.json?v=${ver}`);
    if (!cityRes.ok) return null;
    const cityJSON = await cityRes.json();
    const itemSet = cityJSON.itemSet;
    let tmpl = ITEM_TMPL_CACHE[itemSet];
    if (!tmpl && itemSet) {
      const tmplRes = await fetch(`./data/items_${itemSet}.json?v=${ver}`);
      if (tmplRes.ok) {
        tmpl = await tmplRes.json();
        ITEM_TMPL_CACHE[itemSet] = tmpl;
      }
    }
    const merged = {
      city: cityJSON.city,
      items: tmpl ? tmpl.items : (cityJSON.items || []),
      uncollectible: tmpl ? tmpl.uncollectible : (cityJSON.uncollectible || []),
      itemSet: itemSet || ''
    };
    const norm = normalizeCityData(merged);
    if (norm) CITY_CACHE[cityId] = norm;
    return norm;
  } catch(e) {
    console.error(`v1 load failed for ${cityId}:`, e);
    return null;
  }
}

// Phase B-4: 重複呼出ガード（連打防止）
let _loadingCityId = null;
async function loadCity(id, name, prefCode) {
  if (_loadingCityId === id) return;
  const cityMeta = CITIES.find(c => c.id === id);
  if (cityMeta && !isCityReady(cityMeta)) {
    const prefName = cityMeta.prefName || PREFS.find(p => p.c === (prefCode||id.substring(0,2)))?.n || '';
    showComingSoon(id, name, prefName);
    return;
  }
  // 即時キャッシュ命中（八王子等）はトーストなし
  let data = CITY_CACHE[id];
  if (!data) {
    _loadingCityId = id;
    showToast(lang==='en'?'⏳ Loading…':'⏳ 読み込み中…');
    try {
      data = await loadCityData(id);
    } catch(e) {
      console.error('loadCity error', e);
      data = null;
    }
    _loadingCityId = null;
    if (!data) {
      showToast(lang==='en'?'⚠️ Failed to load city data':'⚠️ '+T('noCityData'));
      return;
    }
  }
  cityData = data;
  // prefCode が未指定なら id の先頭2桁を使用
  const _prefCode = prefCode || id.substring(0,2);
  currentCity = { id, name: data.city.name, pref: data.city.prefecture, prefCode: _prefCode };
  localStorage.setItem(STORAGE_CITY, JSON.stringify({id, name: data.city.name, pref: _prefCode}));
  saveRecent({id, name: data.city.name, pref: data.city.prefecture});
  showApp();
}

