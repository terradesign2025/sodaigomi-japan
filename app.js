// 重量制持込料金シミュレータ（perKgFee/perKgUnit、または carryInFee 文字列から推定）
// 戻り値: {hasSim, html, fee, unit, unitKg}
function buildWeightSimulator(rules) {
  if (!rules) return {hasSim:false, html:''};
  let fee = null;
  let unitKg = null;
  // 1) 構造化データ優先
  if (typeof rules.perKgFee === 'number') {
    fee = rules.perKgFee;
    // perKgUnit例: "10kg" "1kg" "5kg"
    const m = String(rules.perKgUnit || '1kg').match(/(\d+)\s*kg/i);
    unitKg = m ? parseInt(m[1], 10) : 1;
  } else if (typeof rules.carryInFee === 'number') {
    fee = rules.carryInFee;
    unitKg = 1;
  } else {
    // 2) carryInFee/selfCarryIn の文字列から推定
    const txt = String(rules.carryInFee || rules.selfCarryIn || '');
    // パターン: "10kgごと¥350" "10kg=¥200" "1kg=¥42" "10kgあたり300円"
    const m = txt.match(/(\d+)\s*kg(?:ごと|あたり|単位|=)\s*[¥￥]?\s*(\d{1,5})\s*円?/i);
    if (m) {
      unitKg = parseInt(m[1], 10);
      fee = parseInt(m[2], 10);
    }
  }
  if (fee == null || !unitKg || unitKg <= 0) return {hasSim:false, html:''};
  const titleJa = '持込料金シミュレータ';
  const titleEn = 'Weight Fee Simulator';
  const labelJa = '持込kg数';
  const labelEn = 'Weight (kg)';
  const noteJa = `料金単位：${unitKg}kgごとに¥${fee.toLocaleString()}`;
  const noteEn = `Rate: ¥${fee.toLocaleString()} per ${unitKg}kg`;
  const html = `<div class="weight-sim" data-fee="${fee}" data-unit-kg="${unitKg}">
    <div class="weight-sim-hd">⚖️ ${esc(lang==='en'?titleEn:titleJa)}</div>
    <div class="weight-sim-row">
      <label for="wsimKg">${esc(lang==='en'?labelEn:labelJa)}</label>
      <input type="number" id="wsimKg" min="0" step="1" value="10" oninput="calcWeightSim()">
      <span style="font-size:12px;color:var(--t2)">kg</span>
    </div>
    <div class="weight-sim-result" id="wsimResult"></div>
    <div class="weight-sim-note">${esc(lang==='en'?noteEn:noteJa)}</div>
  </div>`;
  return {hasSim:true, html, fee, unitKg};
}

// 重量制シミュレータの再計算（input イベント）
function calcWeightSim() {
  const kgInput = document.getElementById('wsimKg');
  const resultEl = document.getElementById('wsimResult');
  const simEl = resultEl?.closest('.weight-sim');
  if (!kgInput || !resultEl || !simEl) return;
  const kg = parseFloat(kgInput.value);
  const fee = parseFloat(simEl.dataset.fee);
  const unitKg = parseFloat(simEl.dataset.unitKg);
  if (!isFinite(kg) || kg < 0 || !isFinite(fee) || !isFinite(unitKg) || unitKg <= 0) {
    resultEl.innerHTML = '<span style="color:var(--t3)">kg数を入力してください</span>';
    return;
  }
  // 切り上げ単位計算
  const units = Math.ceil(kg / unitKg);
  const total = units * fee;
  const labelJa = `${kg}kg → 約 <strong>¥${total.toLocaleString()}</strong>（${unitKg}kg×${units}単位）`;
  const labelEn = `${kg}kg → approx <strong>¥${total.toLocaleString()}</strong> (${units} unit${units>1?'s':''} of ${unitKg}kg)`;
  resultEl.innerHTML = lang==='en' ? labelEn : labelJa;
}

// 持込施設の住所抽出（selfCarryIn から「施設名（住所）」形式を解析）
// 戻り値: {facility, address, mapUrl} or null
function extractFacilityAddress(rules) {
  const txt = String(rules?.selfCarryIn || '');
  if (!txt) return null;
  // パターン1: "施設名（住所）" or "施設名(住所)"
  let m = txt.match(/([一-龥゠-ヿ぀-ゟA-Za-z0-9・ー]+(?:センター|工場|清掃|処理場|プラント|施設|事務所))[\s（(]+([一-龥゠-ヿ぀-ゟA-Za-z0-9\-ー丁目番地号]+(?:[\d\-]+)?)[\s）)]/);
  if (m) {
    const facility = m[1];
    const address = m[2];
    const q = encodeURIComponent(`${facility} ${address}`);
    return {facility, address, mapUrl: `https://www.google.com/maps/search/?api=1&query=${q}`};
  }
  // パターン2: "施設名" のみ抽出（住所なし）
  m = txt.match(/([一-龥゠-ヿ぀-ゟA-Za-z0-9・ー]{3,30}(?:センター|工場|清掃|処理場|プラント))/);
  if (m) {
    const facility = m[1];
    const cityName = currentCity?.name || '';
    const q = encodeURIComponent(`${facility} ${cityName}`);
    return {facility, address: '', mapUrl: `https://www.google.com/maps/search/?api=1&query=${q}`};
  }
  return null;
}

