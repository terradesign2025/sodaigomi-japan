function showCityList(cities, label) {
  document.getElementById('cityLabel').textContent=label+' › '+(lang==='en'?'Select city':'市区町村を選ぶ');
  const sReady=T('sReady'), sSoon=T('sSoon');
  // 表示順: dataReady を上に、その後で50音順
  const sorted=cities.slice().sort((a,b)=>{
    const ra=isCityReady(a)?0:1, rb=isCityReady(b)?0:1;
    if(ra!==rb) return ra-rb;
    return a.name.localeCompare(b.name,'ja');
  });
  const SPONSORED_CITIES = new Set(['13101','13103','13104','13201']);
  const html=sorted.map(c=>{
    const ready=isCityReady(c);
    const spBadge = SPONSORED_CITIES.has(c.id) ? `<span class="s-sponsored">SP</span>` : '';
    return `<div class="li-row" onclick="loadCity('${c.id}','${c.name}','${c.pref}')">
      <span>${esc(c.name)}</span><span style="display:flex;align-items:center;gap:4px">${spBadge}<span class="${ready?'s-ready':'s-soon'}">${ready?sReady:sSoon}</span></span>
    </div>`;
  }).join('');
  document.getElementById('cityList').innerHTML=html||`<div class="li-row" style="color:var(--t3);cursor:default">${T('sSoon')}</div>`;
  document.getElementById('prefSec').style.display='none';
  document.getElementById('rgSec').style.display='none';
  document.getElementById('citySec').style.display='block';
}

