// Phase B-4: 都市データ読込み済み判定（dataReady / EMBEDDED / TOKYO_V2_AVAILABLE / CITY_CACHE）
function isCityReady(c) {
  if (!c) return false;
  if (c.dataReady) return true;
  if (EMBEDDED[c.id]) return true;
  if (CITY_CACHE[c.id]) return true;
  if (TOKYO_V2_AVAILABLE.has(c.id)) return true;
  return false;
}

// Phase B-4: 検索クエリのマッチング（漢字・ひらがな・カタカナ・ローマ字対応）
function matchCityQuery(c, q) {
  if (!q) return false;
  const qLower = q.toLowerCase();
  const qHira = kataToHira(q);
  // 漢字一致（既存挙動を維持）
  if (c.name && c.name.includes(q)) return true;
  if (c.prefName && c.prefName.includes(q)) return true;
  // 東京都市は kana/romaji 索引を併用
  const t = TOKYO_KANA_INDEX[c.id];
  if (t) {
    if (t.kana && t.kana.includes(qHira)) return true;
    if (t.romaji && t.romaji.toLowerCase().includes(qLower)) return true;
  }
  // 全国都市 ひらがな・ローマ字索引（CITY_KANA）
  const nk = CITY_KANA[c.id];
  if (nk) {
    if (nk[0] && nk[0].includes(qHira)) return true;
    if (nk[1] && nk[1].includes(qLower)) return true;
  }
  return false;
}

async function detectLocation() {
  const btn = document.getElementById('gpsBtn');
  const status = document.getElementById('gpsStatus');
  const el = document.getElementById('citySearchResult');
  if (!navigator.geolocation) {
    status.textContent = '⚠️ お使いのブラウザは位置情報に対応していません';
    return;
  }
  btn.textContent = '⏳';
  btn.disabled = true;
  status.textContent = '📍 現在地を取得中...';
  navigator.geolocation.getCurrentPosition(
    async (pos) => {
      const { latitude: lat, longitude: lon } = pos.coords;
      try {
        // 逆ジオコーディング: 国土地理院API（無料・CORS対応）
        const res = await fetch(`https://mreversegeocoder.gsi.go.jp/reverse-geocoder/LonLatToAddress?lon=${lon}&lat=${lat}`);
        const json = await res.json();
        const r = json.results;
        if (!r) throw new Error('no results');
        const prefCode = String(r.muniCd).slice(0, 2).padStart(2, '0');
        const cityCode = String(r.muniCd).padStart(5, '0');
        const prefName = r.lv01Nm;
        const cityName = r.lv02Nm || r.lv01Nm;
        status.textContent = `📍 ${esc(prefName)} ${esc(cityName)}`;
        // cityCodeで直接ロード試行、なければ名前で検索
        const merged = [...CITIES, ...Object.entries(EMBEDDED).map(([id,d])=>({id,pref:id.substring(0,2),prefName:d.city.prefecture,name:d.city.name,dataReady:true}))]
          .filter((c,i,a)=>a.findIndex(x=>x.id===c.id)===i);
        let hit = merged.find(c => c.id === cityCode);
        if (!hit) hit = merged.find(c => c.prefName === prefName && c.name === cityName);
        if (!hit) hit = merged.find(c => c.prefName && c.prefName.includes(prefName.replace(/[都道府県]/,'')) && cityName && c.name && c.name.includes(cityName.replace(/[市区町村]/,'')));
        if (hit) {
          status.textContent = `📍 ${esc(hit.prefName||'')} ${esc(hit.name)} を検出`;
          el.innerHTML = `<div class="pref-list"><div class="li-row" onclick="loadCity('${hit.id}','${hit.name}','${hit.pref}')">
            ${esc(hit.prefName||'')} ${esc(hit.name)}<span class="${isCityReady(hit)?'s-ready':'s-soon'}">${isCityReady(hit)?T('sReady'):T('sSoon')}</span>
          </div></div>`;
        } else {
          status.textContent = `📍 ${esc(prefName)}${esc(cityName)} はデータ未対応です`;
          el.innerHTML = '';
        }
      } catch(e) {
        status.textContent = '⚠️ 住所の取得に失敗しました';
      }
      btn.textContent = '📍';
      btn.disabled = false;
    },
    (err) => {
      const msg = err.code === 1 ? '位置情報の許可が必要です' : err.code === 2 ? '位置情報を取得できません' : 'タイムアウトしました';
      status.textContent = `⚠️ ${msg}`;
      btn.textContent = '📍';
      btn.disabled = false;
    },
    { timeout: 10000, maximumAge: 60000 }
  );
}

async function lookupZip(zip) {
  const el = document.getElementById('citySearchResult');
  el.innerHTML = `<div class="pref-list"><div class="li-row" style="color:var(--t3);cursor:default">🔍 〒${zip.slice(0,3)}-${zip.slice(3)} を検索中...</div></div>`;
  try {
    const res = await fetch(`https://zipcloud.ibsnet.co.jp/api/search?zipcode=${zip}`);
    const json = await res.json();
    if (!json.results || !json.results.length) {
      el.innerHTML = `<div class="pref-list"><div class="li-row" style="color:var(--t3);cursor:default">〒${zip.slice(0,3)}-${zip.slice(3)} の住所が見つかりません</div></div>`;
      return;
    }
    const r = json.results[0];
    const pref = r.address1;
    const cityRaw = r.address2;
    const merged = [...CITIES, ...Object.entries(EMBEDDED).map(([id,d])=>({id,pref:id.substring(0,2),prefName:d.city.prefecture,name:d.city.name,dataReady:true}))]
      .filter((c,i,a)=>a.findIndex(x=>x.id===c.id)===i);
    const hits = merged.filter(c => {
      if (c.prefName !== pref) return false;
      return c.name === cityRaw || cityRaw.startsWith(c.name) || c.name.startsWith(cityRaw.replace(/[郡].*/,''));
    });
    const sReady = T('sReady'), sSoon = T('sSoon');
    if (!hits.length) {
      el.innerHTML = `<div class="pref-list"><div class="li-row" style="color:var(--t3);cursor:default">〒${zip.slice(0,3)}-${zip.slice(3)}（${esc(pref)}${esc(cityRaw)}）はデータ未対応です</div></div>`;
      return;
    }
    el.innerHTML = `<div class="pref-list">${hits.slice(0,10).map(c=>{
      const ready = isCityReady(c);
      return `<div class="li-row" onclick="loadCity('${c.id}','${c.name}','${c.pref}')">
        ${esc(c.prefName||'')} ${esc(c.name)}<span class="${ready?'s-ready':'s-soon'}">${ready?sReady:sSoon}</span>
      </div>`;
    }).join('')}</div>`;
  } catch(e) {
    el.innerHTML = `<div class="pref-list"><div class="li-row" style="color:var(--t3);cursor:default">郵便番号の検索に失敗しました。通信環境をご確認ください。</div></div>`;
  }
}

